// switch statement

let day = 7;

if (day === 0) {
  console.log("It's Sunday");
} else if (day === 1) {
  console.log("It's Monday");
} else if (day === 2) {
  console.log("It's Tuesday");
} else if (day === 3) {
  console.log("It's Wednesday");
} else if (day === 4) {
  console.log("It's Thursday");
} else if (day === 5) {
  console.log("It's Friday");
} else if (day === 6) {
  console.log("It's Saturday");
} else {
  console.log("It's a Invalid day ");
}

let day1 = 3;

switch (day1) {
  case 0:
    console.log("It's a Sunday");
    break;
  case 1:
    console.log("It's a Monday");
    break;
  case 2:
    console.log("It's a Tuesday");
    break;
  case 3:
    console.log("It's a Wednesday");
    break;
  case 4:
    console.log("It's a Thursday");
    break;
  case 5:
    console.log("It's a Friday");
    break;
  case 6:
    console.log("It's a Saturday");
    break;
  default:
    console.log("It's a Invalid day");
}
